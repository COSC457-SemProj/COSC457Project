    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Employee employee = new Employee();
        String id = SystemInterface.generateUniqueId(employee.getTableName());
        employee.setEmpNum(id);
        employee.setEmpFName(empFNameBox.getText());
        employee.setEmpMName(empMNameBox.getText());
        employee.setEmpLName(empLNameBox.getText());
        employee.setEmpSSN(empSSNBox.getText());
        employee.setEmpDoB(empDoBBox.getText());
        employee.setEmpPoB(empPoBBox.getText());
        employee.setEmpAddress(empAddressBox.getText());
        employee.setEmpZipCode(empZipCodeBox.getText());
        employee.setEmpEmergencyPhone(empEmergencyPhoneBox.getText());
        employee.setEmpEmergencyPoC(empEmergencyPoCBox.getText());
        employee.setEmpTitle(empTitleBox.getText());
        employee.setEmpGender(empGenderBox.getText());
        employee.setEmpHomePhone(empHomePhoneBox.getText());
        employee.setEmpCellPhone(empCellPhoneBox.getText());
        int monthNum = getMonthNum();
        employee.setEmpAnniversaryMonth(Integer.toString(monthNum));
        employee.setEmpSixMonthReview(Integer.toString((monthNum + 6) % 12));
        employee.setEmpAnnualReview(Integer.toString(monthNum));
        employee.setEmpSalary(empSalaryBox.getText());
        employee.setEmpVeteranStatus(empVeteranStatusBox.getText());

        Race race = new Race();
        race.setRace(empRaceBox.getText());
        race.setRaceEmpNum(id);
        if(SystemInterface.insert(employee) == null){
            JOptionPane.showMessageDialog(null, "Error, check your data and try again");
        }else{
            SystemInterface.insert(race);
            JOptionPane.showMessageDialog(null, "Insert Successful, the Employee ID is " + id);
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

