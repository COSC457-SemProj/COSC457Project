    private int getMonthNum(){
        String selectedItem = (String) empAnniversaryMonthBox.getSelectedItem();
        for(int i = 0; i < 12; i++){
            if(selectedItem.equals(months[i])){
                return i;
            }
        }
        return -1;
    }

